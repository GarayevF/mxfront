/* ========== LANGUAGE SWITCHING ========== */
function setLanguage(lang) {
    if (!translations[lang]) return;
    currentLang = lang;
    localStorage.setItem("mamaxx_lang", lang);
    document.documentElement.lang = lang;

    document.querySelectorAll("[data-i18n]").forEach(function(el) {
        var key = el.getAttribute("data-i18n");
        if (translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });

    // Translate placeholders
    document.querySelectorAll("[data-i18n-placeholder]").forEach(function(el) {
        var key = el.getAttribute("data-i18n-placeholder");
        if (translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });

    // Update language button display
    var flag = document.getElementById("langFlag");
    var code = document.getElementById("langCode");
    if (flag) flag.textContent = langMeta[lang].flag;
    if (code) code.textContent = langMeta[lang].code;

    // Update active states
    document.querySelectorAll(".lang-option").forEach(function(btn) {
        btn.classList.toggle("active", btn.dataset.lang === lang);
    });
    document.querySelectorAll(".mobile-lang-btn").forEach(function(btn) {
        btn.classList.toggle("active", btn.dataset.lang === lang);
    });

    // Close dropdown
    var dropdown = document.getElementById("langDropdown");
    if (dropdown) dropdown.classList.remove("open");
}

